public class Exchange {
    private double eur_ron = 4.81;
    private double usd_ron = 4.7;
    private double gbp_ron = 5.9;

    public double getEur_ron() {
        return eur_ron;
    }

    public double getUsd_ron() {
        return usd_ron;
    }

    public double getGbp_ron() {
        return gbp_ron;
    }

    public void setEur_ron(double eur_ron) {
        this.eur_ron = eur_ron;
    }

    public void setUsd_ron(double usd_ron) {
        this.usd_ron = usd_ron;
    }

    public void setGbp_ron(double gbp_ron) {
        this.gbp_ron = gbp_ron;
    }

}
